delai<-function(date1,date2)
{
h1<-as.numeric(substr(substr(date1,12,19),1,2))
m1<-as.numeric(substr(substr(date1,12,19),4,5))
s1<-as.numeric(substr(substr(date1,12,19),7,8))
j1<-as.numeric(substr(date1,9,10))
dates1<-j1*24*3600+h1*3600+m1*60+s1
h2<-as.numeric(substr(substr(date2,12,19),1,2))
m2<-as.numeric(substr(substr(date2,12,19),4,5))
s2<-as.numeric(substr(substr(date2,12,19),7,8))
j2<-as.numeric(substr(date2,9,10))
dates2<-j2*24*3600+h2*3600+m2*60+s2
delai<-dates2-dates1
delaih<-floor(delai/3600)
delaim<-floor((delai-delaih*3600)/60)
delais<-floor(delai-delaih*3600-delaim*60)
cat(delaih,"h",delaim,"min",delais,"s","\n")
}
